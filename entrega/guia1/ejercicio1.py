# j. Imprimir un texto con caracteres especiales como salto de línea (\n) o
# tabulación (\t), que nos permita imprimir en forma de escalera Feliz Primavera
# 2024.
# Feliz
#       Primavera
#                   2024
print('Ejercicio j', end=": \n")
print("Feliz", "\tPrimavera", "\t\t\t2024", sep="\n")

# k. Escribir un programa que imprima con un print “Solo podemos ver poco del
