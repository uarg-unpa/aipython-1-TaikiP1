# 16. Usar los métodos capitalize(), title(), swapcase() para formatear el valor del string
# “sometimes it is the people no one imagines anything of who do the things that no
# one can imagine.”

oracion = "sometimes it is the people no one imagines anything of who do the things that no one can imagine."

print("Capitalize: ", oracion.capitalize())
print("Title: ", oracion.title())
print("Swapcase: ", oracion.swapcase())
